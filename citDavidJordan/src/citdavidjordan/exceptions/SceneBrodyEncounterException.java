public class SceneBrodyEncounterException extends Exception{

    public SceneBrodyEncounterException() {
    }

    public SceneBrodyEncounterException(String message) {
        super(message);
    }

    public SceneBrodyEncounterException(String message, Throwable cause) {
        super(message, cause);
    }

    public SceneBrodyEncounterException(Throwable cause) {
        super(cause);
    }

    public SceneBrodyEncounterException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
    
    
    
}
