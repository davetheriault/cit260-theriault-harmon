package citdavidjordan.exceptions;

/**
 *
 * @author Deann_2
 */
public class Scene2NumberException extends Exception {
    
    public Scene2NumberException() {
    }

    public Scene2NumberException(String message) {
        super(message);
    }

    public Scene2NumberException(String message, Throwable cause) {
        super(message, cause);
    }

    public Scene2NumberException(Throwable cause) {
        super(cause);
    }

    public Scene2NumberException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

    
    
}
