package citdavidjordan.exceptions;

/**
 *
 * @author Theriault
 */
public class Scene4RPSException extends Exception{

    public Scene4RPSException() {
    }

    public Scene4RPSException(String message) {
        super(message);
    }

    public Scene4RPSException(String message, Throwable cause) {
        super(message, cause);
    }

    public Scene4RPSException(Throwable cause) {
        super(cause);
    }

    public Scene4RPSException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
    
    
    
}
