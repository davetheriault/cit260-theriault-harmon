package citdavidjordan.exceptions;

/**
 *
 * @author Deann_2
 */
public class Scene8NaotoException extends Exception{

    public Scene8NaotoException() {
    }

    public Scene8NaotoException(String message) {
        super(message);
    }

    public Scene8NaotoException(String message, Throwable cause) {
        super(message, cause);
    }

    public Scene8NaotoException(Throwable thrwbl) {
        super(thrwbl);
    }

    public Scene8NaotoException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
    
}
